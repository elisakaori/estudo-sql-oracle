--Exercício 1 - k)
SELECT COD_PRODUTO, MIN(DESCONTO) AS MENOR, MAX(DESCONTO) AS MAIOR, AVG(DESCONTO) AS MEDIA
FROM TAB_PEDIDO
GROUP BY COD_PRODUTO;

--Exercício 1 - l)
