SELECT ID_NF, ID_ITEM, COD_PRODUTO, VALOR_UNIT FROM TAB_PEDIDO WHERE DESCONTO IS NULL;

--Exercício 1 - b)
SELECT ID_NF, ID_ITEM, COD_PRODUTO, VALOR_UNIT, (VALOR_UNIT*(DESCONTO/100)) 
FROM TAB_PEDIDO 
WHERE DESCONTO IS NOT NULL;

--Exercício 1 - c)
UPDATE TAB_PEDIDO SET DESCONTO = 0
WHERE DESCONTO IS NULL; 

--Exercício 1 - d)
SELECT ID_NF, ID_ITEM, COD_PRODUTO, VALOR_UNIT, (QUANTIDADE *
VALOR_UNIT) AS VALOR_TOTAL, DESCONTO, (VALOR_UNIT*(DESCONTO/100)) AS VALOR_VENDIDO
FROM TAB_PEDIDO;

--Exercício 1 - e)
SELECT SUM(QUANTIDADE * VALOR_UNIT) AS VALOR_TOTAL
FROM TAB_PEDIDO
GROUP BY ID_NF
ORDER BY VALOR_TOTAL DESC;

--Exercício 1 - f)
SELECT ID_NF, SUM(VALOR_UNIT - (VALOR_UNIT*(DESCONTO/100))) AS VALOR_VENDIDO
FROM TAB_PEDIDO
GROUP BY ID_NF
ORDER BY VALOR_VENDIDO DESC;

--Exercício 1 - g)
SELECT COD_PRODUTO, SUM(QUANTIDADE)
FROM TAB_PEDIDO
GROUP BY COD_PRODUTO;

--Exercício 1 - h)
SELECT ID_NF, COD_PRODUTO, QUANTIDADE
FROM TAB_PEDIDO
WHERE QUANTIDADE > 10
GROUP BY ID_NF, COD_PRODUTO; -- só da certo se tiver QUANTIDADE

--Exercício 1 - i)
SELECT ID_NF, SUM(VALOR_UNIT * QUANTIDADE) AS VALOR_TOTAL 
FROM TAB_PEDIDO 
HAVING SUM(VALOR_UNIT * QUANTIDADE) > 500
GROUP BY ID_NF
ORDER BY VALOR_TOTAL DESC;

--Exercício 1 - j)
SELECT COD_PROD, AVG(DESCONTO) AS MEDIA
FROM TAB_PEDIDO
